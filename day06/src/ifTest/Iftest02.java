package ifTest;

// 10번 : if~else문
public class Iftest02 { // 클래스 시작

	public static void main(String[] args) { // main 시작

		System.out.println("출력 시작"); // 출력문

		if (false) { // if문 영역
			System.out.println("조건식이 true입니다."); // if문의 조건식이 참이면 이 영역의 출력값이 나온다.
		} 
		
		else { // else문 영역
			System.out.println("조건식이 false입니다."); // if문의 조건식이 거짓이면 이 영역의 출력값이 나온다.
		}

		System.out.println("출력 끝"); // 출력 종료

	}

}
